const iconMap = {
  "/": IconHome,
  "/log": IconLog,
  "/community": IconCommunity,
  "/members": IconMembers,
  "/dm": IconChat,
  "/me": IconProfile,
  "/admin": IconAdmin,
} as const;

export function NavIcon({ href, className }: { href: string; className?: string }) {
  const Icon = href in iconMap ? iconMap[href as keyof typeof iconMap] : null;
  if (!Icon) return null;
  return <Icon className={className} />;
}
